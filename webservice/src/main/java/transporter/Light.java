package transporter;

public class Light 
{
    int id;
    int deviceId;
    int timeId;
    int lux;

    public int getId() 
    {
        return id;
    }

    public void setId(int id) 
    {
        this.id = id;
    }

    public int getDeviceId() 
    {
        return deviceId;
    }

    public void setDeviceId(int deviceId) 
    {
        this.deviceId = deviceId;
    }

    public int getTimeId() 
    {
        return timeId;
    }

    public void setTimeId(int timeId) 
    {
        this.timeId = timeId;
    }

    public int getLux() 
    {
        return lux;
    }

    public void setLux(int lux) 
    {
        this.lux = lux;
    }
    
}
